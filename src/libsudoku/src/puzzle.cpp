#include "sudoku/puzzle.h"

/* ************************************************************************* */

using Sudoku::Puzzle;
using Sudoku::PuzzleWidth;
using Sudoku::PuzzleHeight;

/* ************************************************************************* */
/* ************************************************************************* */

Puzzle::Puzzle()
{
    for (int y = 0; y < PuzzleHeight; y++)
    {
        for (int x = 0; x < PuzzleWidth; x++)
        {
            Cell(x, y) = 0;
        }
    }
}

